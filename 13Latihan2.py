# operasi AND
hasil1 = (5 > 6) and (10 < 8)
print('+-OPERASI AND-+')
print('Apakah pernyataan bahwa (5>6) DAN (10<8) adalah benar? =>',hasil1)
print('\n')

# operasi OR
hasil2 = ('duniailkom' == 'duniailkom') or (10 < 8)
print("+-OPERASI OR-+")
print("Apakah pernyataan bahwa (duniailkom == duniailkom) ATAU (10<8) adalah benar? =>",hasil2)
print('\n')

# operasi NOT
hasil3 = not (10 < 10)
print("+-OPERASI NOT-+")
print("Apakah kebalikan dari BUKAN (10 < 10) ? =>",hasil3)
print('\n')

